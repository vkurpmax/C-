#include <iostream>
using namespace std;

int main()
{
	int age = 20;
	if (age > 16 && age < 60) {
		cout << "Accepted" << endl;
	}
	/*Outputs "Accepted"*/
}
/*THE AND OPERATOR
In the example above, the logical AND operator was used to combine both expressions.

The expression in the if statement evaluates to "true" only if both expressions are "true".*/
