#include <iostream>
using namespace std;

int main()
{
	int num = 1;
	while (num < 6)
	{
		cout << "Number: " << num << endl;
		num++;
	}

	/* Outputs
	Number: 1
	Number: 2
	Number: 3
	Number: 4
	Number: 5
	*/
}

/*USING INCREMENT OR DECREMENT

The increment or decrement operators can be used to change values in the loop.

num++ is equivalent to num = num + 1*/
